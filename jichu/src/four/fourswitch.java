package four;

public class fourswitch {
     public static void main(String[] args) {
    	 int i=1;
    	 int max=5;
		for(i=1;i<max;i++){
			switch (i) {
			case 1:
				System.out.println(i);
				
			case 2:
				System.out.println(i);
				
			case 3:
				System.out.println(i);
			
			case 4:
				System.out.println(i);
			
			default:
			}
		}
	}
}
